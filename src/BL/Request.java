package BL;

/**
 *
 * @author alexandre
 */
public class Request {
   private String desc;
   private String etat;
   
   public Request(String desc,String etat){
       this.desc=desc;
       this.etat=etat;
   }

    /**
     * @return the desc
     */
    public String getDesc() {
        return desc;
    }

    /**
     * @param desc the desc to set
     */
    public void setDesc(String desc) {
        this.desc = desc;
    }

    /**
     * @return the etat
     */
    public String getEtat() {
        return etat;
    }

    /**
     * @param etat the etat to set
     */
    public void setEtat(String etat) {
        this.etat = etat;
    }
}